void solve() {
  int n;
  cin >> n;
  string s;
  cin >> s;
  if (n < 11) {
    output("NO");
    return;
  }
  for (int i = n - 11; i >= 0; --i) {
    if (s[i] == '8') {
      output("YES");
      return;
    }
  }
  output("NO");
}

